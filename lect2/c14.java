//  Check the given number is Positive, Negative or Zero

package lect2;

public class c14 {

    public static void main(String[] args)
    {

        int num = 1;

        if(num > 0)
        {
            System.out.println("Positive");

        }
        else
        {
            if(num < 0)
            {
                System.out.println("Negative");
            }
            else
            {
                System.out.println("Zero");
            }
        }

    }
    
}
